int perf_stats_enabled(void) { return g_perf_enabled; }

void perf_stats_init(void) {
    const char* env = getenv(PERF_STATS_ENV);
    g_perf_enabled = (env && (strcmp(env, "1") == 0 || strcmp(env, "true") == 0));
    if (!g_perf_enabled) return;
    memset(g_perf_counts, 0, sizeof(g_perf_counts));
    g_last_print = time(NULL);
}

void perf_stats_print(void) {
    if (!g_perf_enabled) return;
    
    time_t now = time(NULL);
    if (now - g_last_print < 1) return;
    
    pthread_mutex_lock(&g_mutex);
    if (now - g_last_print < 1) { pthread_mutex_unlock(&g_mutex); return; }
    g_seconds++;
    g_last_print = now;
    
    uint64_t total = 0;
    for (int i = 0; i < P_MAX; i++) total += g_perf_counts[i];
    
    if (total == 0) { pthread_mutex_unlock(&g_mutex); return; }
    
    printf("\n========== LASX Stats (Second %d) Total: %lu ==========\n", g_seconds, total);
    printf("%-20s %12s %6s\n", "Instruction", "Count", "Pct");
    printf("----------------------------------------\n");
    
    // 找 top 20
    for (int r = 0; r < 20; r++) {
        uint64_t max = 0; int mi = -1;
        for (int i = 0; i < P_MAX; i++)
            if (g_perf_counts[i] > max) { max = g_perf_counts[i]; mi = i; }
        if (mi < 0 || max == 0) break;
        const char* name = idx_to_name[mi] ? idx_to_name[mi] : "OTHER";
        printf("%-20s %12lu %5.1f%%\n", name, max, max * 100.0 / total);
        g_perf_counts[mi] = 0;
    }
    printf("============================================\n\n");
    
    memset(g_perf_counts, 0, sizeof(g_perf_counts));
    pthread_mutex_unlock(&g_mutex);
}

void perf_stats_cleanup(void) {}
